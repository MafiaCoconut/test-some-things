// Helper functions for filtering and searching
export const getUniqueYears = () => {
  const years = monsterHighReleases.map(release => release.year);
  return [...new Set(years)].sort((a, b) => b - a); // Most recent first
};

export const getUniqueCharacters = () => {
  const characters = monsterHighReleases.map(release => release.character);
  return [...new Set(characters)].sort();
};

export const getUniqueSeries = () => {
  const series = monsterHighReleases.map(release => release.series);
  return [...new Set(series)].sort();
};

export const getTierTypes = () => {
  const tiers = monsterHighReleases.map(release => release.tierType);
  return [...new Set(tiers)].sort();
};

export const getPackTypes = () => {
  const packs = monsterHighReleases.map(release => release.packType);
  return [...new Set(packs)].sort();
};

// Search and filter functions
export const searchReleases = (releases, searchTerm) => {
  if (!searchTerm) return releases;
  
  const term = searchTerm.toLowerCase();
  return releases.filter(release => 
    release.name.toLowerCase().includes(term) ||
    release.character.toLowerCase().includes(term) ||
    release.series.toLowerCase().includes(term) ||
    release.tags.some(tag => tag.toLowerCase().includes(term)) ||
    release.description.toLowerCase().includes(term)
  );
};

export const filterReleases = (releases, filters) => {
  let filtered = [...releases];
  
  if (filters.year) {
    filtered = filtered.filter(release => release.year === filters.year);
  }
  
  if (filters.character) {
    filtered = filtered.filter(release => release.character === filters.character);
  }
  
  if (filters.series) {
    filtered = filtered.filter(release => release.series === filters.series);
  }
  
  if (filters.tierType) {
    filtered = filtered.filter(release => release.tierType === filters.tierType);
  }
  
  if (filters.packType) {
    filtered = filtered.filter(release => release.packType === filters.packType);
  }
  
  if (filters.exclusive !== undefined) {
    filtered = filtered.filter(release => release.exclusive === filters.exclusive);
  }
  
  return filtered;
};

export const sortReleases = (releases, sortBy) => {
  const sorted = [...releases];
  
  switch (sortBy) {
    case 'newest':
      return sorted.sort((a, b) => new Date(b.releaseDate) - new Date(a.releaseDate));
    case 'oldest':
      return sorted.sort((a, b) => new Date(a.releaseDate) - new Date(b.releaseDate));
    case 'name-asc':
      return sorted.sort((a, b) => a.name.localeCompare(b.name));
    case 'name-desc':
      return sorted.sort((a, b) => b.name.localeCompare(a.name));
    case 'character':
      return sorted.sort((a, b) => a.character.localeCompare(b.character));
    default:
      return sorted;
  }
};